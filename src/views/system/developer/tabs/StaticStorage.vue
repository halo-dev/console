<template>
  <div class="option-tab-wrapper">
    <a-card
      :bordered="false"
      :bodyStyle="{ padding: 0 }"
    >
      <div class="table-operator">
        <a-button
          type="primary"
          icon="cloud-upload"
          @click="() => (uploadVisible = true)"
        >上传</a-button>
        <a-button
          icon="plus"
          @click="handleShowCreateFolderModal({})"
        >
          新建文件夹
        </a-button>
        <a-button
          icon="sync"
          @click="handleListStatics"
          :loading="loading"
        >
          刷新
        </a-button>
      </div>
      <div class="mt-4">
        <a-table
          :rowKey="record => record.id"
          :columns="columns"
          :dataSource="sortedStatics"
          :pagination="false"
          size="middle"
          :loading="loading"
        >
          <span
            slot="name"
            slot-scope="name"
          >
            <ellipsis
              :length="64"
              tooltip
            >
              {{ name }}
            </ellipsis>
          </span>
          <span
            slot="createTime"
            slot-scope="createTime"
          >
            {{ createTime | moment }}
          </span>
          <span
            slot="action"
            slot-scope="text, record"
          >
            <a
              href="javascript:void(0);"
              v-if="!record.isFile"
              @click="handleUpload(record)"
            >上传</a>
            <a
              :href="options.blog_url+record.relativePath"
              target="_blank"
              v-else
            >访问</a>
            <a-divider type="vertical" />
            <a-dropdown :trigger="['click']">
              <a
                href="javascript:void(0);"
                class="ant-dropdown-link"
              >更多</a>
              <a-menu slot="overlay">
                <a-menu-item
                  key="1"
                  v-if="!record.isFile"
                >
                  <a
                    href="javascript:void(0);"
                    @click="handleShowCreateFolderModal(record)"
                  >创建文件夹</a>
                </a-menu-item>
                <a-menu-item key="2">
                  <a-popconfirm
                    :title="record.isFile?'你确定要删除该文件？':'你确定要删除该文件夹？'"
                    okText="确定"
                    cancelText="取消"
                    @confirm="handleDelete(record.relativePath)"
                  >
                    <a href="javascript:void(0);">删除</a>
                  </a-popconfirm>
                </a-menu-item>
                <a-menu-item key="3">
                  <a
                    href="javascript:void(0);"
                    @click="handleShowRenameModal(record)"
                  >重命名</a>
                </a-menu-item>
                <a-menu-item
                  key="4"
                  v-if="record.isFile"
                >
                  <a
                    href="javascript:void(0);"
                    @click="handleShowEditModal(record)"
                  >编辑</a>
                </a-menu-item>
              </a-menu>
            </a-dropdown>
          </span>
        </a-table>
      </div>
    </a-card>
    <a-modal
      title="上传文件"
      v-model="uploadVisible"
      :footer="null"
      :afterClose="onUploadClose"
      destroyOnClose
    >
      <FilePondUpload
        ref="upload"
        name="file"
        :uploadHandler="uploadHandler"
        :filed="selectedFile.relativePath"
      ></FilePondUpload>
    </a-modal>
    <a-modal
      v-model="directoryForm.visible"
      :afterClose="onDirectoryFormModalClose"
      title="创建文件夹"
    >
      <template slot="footer">
        <ReactiveButton
          @click="handleCreateDirectory"
          @callback="handleCreateDirectoryCallback"
          :loading="directoryForm.saving"
          :errored="directoryForm.saveErrored"
          text="创建"
          loadedText="创建成功"
          erroredText="创建失败"
        ></ReactiveButton>
      </template>
      <a-form-model
        ref="directoryForm"
        :model="directoryForm.model"
        :rules="directoryForm.rules"
        layout="vertical"
      >
        <a-form-model-item
          prop="name"
          label="文件夹名："
        >
          <a-input
            ref="createFoldeInput"
            v-model="directoryForm.model.name"
            @keyup.enter="handleCreateDirectory"
          />
        </a-form-model-item>
      </a-form-model>
    </a-modal>
    <a-modal
      v-model="renameModal"
      :afterClose="onRenameClose"
      title="重命名"
    >
      <template slot="footer">
        <a-button
          key="submit"
          type="primary"
          @click="handleRename()"
        >重命名</a-button>
      </template>
      <a-form layout="vertical">
        <a-form-item :label="renameFile?'文件名：':'文件夹名：'">
          <a-input
            ref="renameModalInput"
            v-model="renameName"
            @keyup.enter="handleRename"
          />
        </a-form-item>
      </a-form>
    </a-modal>
    <a-modal
      v-model="editModal"
      title="编辑文件"
      width="80%"
      style="max-width: 1000px"
      :maskClosable="false"
      :keyboard="false"
      :closable="false"
    >
      <template slot="footer">
        <a-popconfirm
          title="未保存的内容将会丢失，确定要退出吗？"
          okText="确定"
          cancelText="取消"
          @confirm="handleEditClose"
        >
          <a-button>取消</a-button>
        </a-popconfirm>
        <a-button
          key="submit"
          type="primary"
          @click="handleEditSave()"
        >保存</a-button>
      </template>
      <a-form layout="vertical">
        <a-form-item>
          <codemirror
            ref="editor"
            :value="editContent"
            :options="codemirrorOptions"
          ></codemirror>
        </a-form-item>
      </a-form>
    </a-modal>
  </div>
</template>
<script>
import Vue from 'vue'
import { mapGetters } from 'vuex'
import staticApi from '@/api/static'
import { codemirror } from 'vue-codemirror-lite'
const context = require.context('codemirror/mode', true, /\.js$/)
context.keys().map(context)
const columns = [
  {
    title: '文件名',
    dataIndex: 'name',
    scopedSlots: { customRender: 'name' },
  },
  {
    title: '文件类型',
    dataIndex: 'mimeType',
    scopedSlots: { customRender: 'mimeType' },
  },
  {
    title: '上传时间',
    dataIndex: 'createTime',
    width: '200px',
    scopedSlots: { customRender: 'createTime' },
  },
  {
    title: '操作',
    dataIndex: 'action',
    width: '120px',
    scopedSlots: { customRender: 'action' },
  },
]
export default {
  components: {
    codemirror,
  },
  name: 'StaticStorage',
  data() {
    return {
      columns: columns,
      statics: [],
      loading: false,
      uploadHandler: staticApi.upload,
      uploadVisible: false,
      selectedFile: {},
      renameModal: false,
      renameName: '',
      renameFile: false,
      codemirrorOptions: {
        tabSize: 4,
        lineNumbers: true,
        line: true,
      },
      editModal: false,
      editContent: '',
      CodeMirror: null,

      directoryForm: {
        model: {
          name: null,
        },
        visible: false,
        saving: false,
        saveErrored: false,
        rules: {
          name: [{ required: true, message: '* 文件夹名不能为空', trigger: ['change'] }],
        },
      },
    }
  },
  beforeMount() {
    this.handleListStatics()
    this.CodeMirror = require('codemirror')
    this.CodeMirror.modeURL = 'codemirror/mode/%N/%N.js'
  },
  computed: {
    ...mapGetters(['options']),
    sortedStatics() {
      const data = this.statics.slice(0)
      return data.sort(function(a, b) {
        return a.isFile - b.isFile
      })
    },
  },
  methods: {
    handleListStatics() {
      this.loading = true
      staticApi
        .list()
        .then((response) => {
          this.statics = response.data.data
        })
        .finally(() => {
          setTimeout(() => {
            this.loading = false
          }, 200)
        })
    },
    handleDelete(path) {
      staticApi
        .delete(path)
        .then((response) => {
          this.$message.success(`删除成功！`)
        })
        .finally(() => {
          this.handleListStatics()
        })
    },
    handleUpload(file) {
      this.selectedFile = file
      this.uploadVisible = true
    },
    handleShowCreateFolderModal(file) {
      this.selectedFile = file
      this.directoryForm.visible = true
      const that = this
      Vue.nextTick().then(() => {
        that.$refs.createFoldeInput.focus()
      })
    },
    handleShowRenameModal(file) {
      this.selectedFile = file
      this.renameName = file.name
      this.renameFile = file.isFile
      this.renameModal = true
      const that = this
      Vue.nextTick().then(() => {
        const inputRef = that.$refs.renameModalInput
        const tmp = inputRef.value.split('.')
        inputRef.focus()
        if (tmp.length <= 1) {
          inputRef.$el.setSelectionRange(0, inputRef.value.length)
        } else {
          inputRef.$el.setSelectionRange(0, inputRef.value.length - tmp.pop().length - 1)
        }
      })
    },
    handleShowEditModal(file) {
      this.selectedFile = file
      const arr = file.name.split('.')
      const postfix = arr[arr.length - 1]
      staticApi.getContent(this.options.blog_url + file.relativePath).then((response) => {
        this.editContent = response.data
        const info = this.CodeMirror.findModeByExtension(postfix)
        if (info === undefined) {
          this.$message.error(`不支持编辑 "${postfix}" 类型的文件`)
        } else {
          this.editModal = true
          Vue.nextTick().then(() => {
            const editor = this.$refs.editor.editor
            editor.setOption('mode', info.mime)
            this.CodeMirror.autoLoadMode(editor, info.mode)
          })
        }
      })
    },
    handleCreateDirectory() {
      const _this = this
      _this.$refs.directoryForm.validate((valid) => {
        if (valid) {
          this.directoryForm.saving = true
          staticApi
            .createFolder(_this.selectedFile.relativePath, _this.directoryForm.model.name)
            .catch(() => {
              _this.directoryForm.saveErrored = true
            })
            .finally(() => {
              setTimeout(() => {
                this.directoryForm.saving = false
              }, 400)
            })
        }
      })
    },
    handleCreateDirectoryCallback() {
      if (this.directoryForm.saveErrored) {
        this.directoryForm.saveErrored = false
      } else {
        this.directoryForm.model = {}
        this.directoryForm.visible = false
        this.handleListStatics()
      }
    },
    handleRename() {
      staticApi
        .rename(this.selectedFile.relativePath, this.renameName)
        .then((response) => {
          this.$message.success(`重命名成功！`)
          this.renameModal = false
        })
        .finally(() => {
          this.handleListStatics()
        })
    },
    handleEditSave() {
      staticApi.save(this.selectedFile.relativePath, this.$refs.editor.editor.getValue()).then((response) => {
        this.$message.success(`文件保存成功！`)
        this.editModal = false
      })
    },
    onDirectoryFormModalClose() {
      this.selectedFile = {}
      this.directoryForm.model.name = null
    },
    onRenameClose() {
      this.selectedFile = {}
      this.renameName = ''
    },
    onUploadClose() {
      this.$refs.upload.handleClearFileList()
      this.selectedFile = {}
      this.handleListStatics()
    },
    handleEditClose() {
      this.editModal = false
      this.selectedFile = {}
      this.editContent = ''
    },
  },
}
</script>
